        /// <summary>
        /// 批量插入数据
        /// </summary>
        /// <param name="dt">记录集</param>
        /// <param name="tableName">表名</param>
        /// <param name="ht">映射关系（key为原列索引值，value为目标列索引值）</param>
        public static void SqlDataTableCopy(DataTable dt, string tableName, Hashtable ht)
        {
            using (SqlConnection connection = new SqlConnection(connectionString))
            {
                connection.Open();
                using (SqlBulkCopy bcp = new SqlBulkCopy(connection))
                {
                    bcp.DestinationTableName = tableName;
                    foreach (DictionaryEntry myDE in ht)
                    {
                        bcp.ColumnMappings.Add(new SqlBulkCopyColumnMapping(myDE.Key.ToString(), myDE.Value.ToString()));
                    }
                    bcp.WriteToServer(dt);
                }
                connection.Close();
            }
        }

